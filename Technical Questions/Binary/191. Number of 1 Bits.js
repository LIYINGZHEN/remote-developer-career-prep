/**
 * @param {number} n - a positive integer
 * @return {number}
 */
var hammingWeight = function(n) {
  let res = 0;

  // 0   -> 00000000000000000000000000001011
  // n-1 -> 00000000000000000000000000001010
  // 1   -> 00000000000000000000000000001010
  // n-1 -> 00000000000000000000000000001001
  // 2   -> 00000000000000000000000000001000
  // n-1 -> 00000000000000000000000000000111
  // 3   -> 00000000000000000000000000000000

  while (n !== 0) {
    n &= n - 1;
    res++;
  }

  return res;
};
